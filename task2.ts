let fruits = ["Apple","Mango","Banana","Orange","Grapes"]
console.log(fruits);
//appending a string at the end of the array:
fruits.push("Avocado");
console.log(fruits);
//prepending a string at the start of an array:
fruits.unshift("Pine apple");
console.log(fruits);
//Removing a string from a specific index in the array and replace it with another string:
fruits.splice(2,1,"Melon");
console.log(fruits);